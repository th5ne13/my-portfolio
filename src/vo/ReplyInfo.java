package vo;

public class ReplyInfo {
	private int br_num, bl_num;
	private String ml_id, br_content, br_isview, br_ip, br_date;
	
	public int getBr_num() {
		return br_num;
	}
	public void setBr_num(int br_num) {
		this.br_num = br_num;
	}
	public int getBl_num() {
		return bl_num;
	}
	public void setBl_num(int bl_num) {
		this.bl_num = bl_num;
	}
	public String getMl_id() {
		return ml_id;
	}
	public void setMl_id(String ml_id) {
		this.ml_id = ml_id;
	}
	public String getBr_content() {
		return br_content;
	}
	public void setBr_content(String br_content) {
		this.br_content = br_content;
	}
	public String getBr_isview() {
		return br_isview;
	}
	public void setBr_isview(String br_isview) {
		this.br_isview = br_isview;
	}
	public String getBr_ip() {
		return br_ip;
	}
	public void setBr_ip(String br_ip) {
		this.br_ip = br_ip;
	}
	public String getBr_date() {
		return br_date;
	}
	public void setBr_date(String br_date) {
		this.br_date = br_date;
	}
}
